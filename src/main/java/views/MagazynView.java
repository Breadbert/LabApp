    private void aktualizujDaneWybranegoTowaru(){
        if(wybranyTowar ==  null){
            JOptionPane.showConfirmDialog(rootPane, "Nie wybrano towaru.");
            return;
        } 
        nazwaTowaruTextField.setText(wybranyTowar.getNazwa());
        cenaTextField.setText(String.valueOf(wybranyTowar.getCena()));
        numerWybranegoElementuLabel.setText(String.valueOf(towary.size()));
    }
    
    private Object dodajTowar(Towar towar){
        towary.add(towar);
        wybranyTowar = towary.get(towary.size() - 1);
        aktualizujDaneWybranegoTowaru();
        iloscElementowLabel.setText(String.valueOf(towary.size()));
        return null;
    }

