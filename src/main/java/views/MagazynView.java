    private void NastepnyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NastepnyButtonActionPerformed
        int index;
        if (wybranyTowar == null){
            index = 0;
        } else {
            index = towary.indexOf(wybranyTowar) + 1; 
            if (index >= towary.size()){
                index = 0;
            }
        }
        wybranyTowar = towary.get(index);
        aktualizujDaneWybranegoTowaru();
    }//GEN-LAST:event_NastepnyButtonActionPerformed

    private void UsunButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UsunButtonActionPerformed

        if(wybranyTowar ==  null){
            JOptionPane.showConfirmDialog(this, "Nie wybrano towaru.");
            return;
        } 
        towary.remove(wybranyTowar);
        try{
            PoprzedniButtonActionPerformed(evt);
        }
        catch (IndexOutOfBoundsException ex){
            
        }

    }//GEN-LAST:event_UsunButtonActionPerformed

    private void PoprzedniButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PoprzedniButtonActionPerformed
        int index;
        if (wybranyTowar == null){
            index = 0;
        } else {
            index = towary.indexOf(wybranyTowar) - 1; 
            if (index < 0){
                index = towary.size()-1;
            }
        }
        wybranyTowar = towary.get(index);
        aktualizujDaneWybranegoTowaru();
    }//GEN-LAST:event_PoprzedniButtonActionPerformed

